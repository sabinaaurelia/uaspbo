// Form validation method
private boolean isFormValid() {
    if (idS.getText().equals("") || nama.getText().equals("") || harga.getText().equals("") || merk.getText().equals("") || stok.getText().equals("")) {
        JOptionPane.showMessageDialog(null, "Data empty!!!", "Data Validation", JOptionPane.INFORMATION_MESSAGE);
        return false;
    }
    if (!validasiInput() || !validasiInput1() || !validasiInput2()) {
        return false;
    }
    if (!containsOnlyLettersAndSpace(nama.getText())) {
        JOptionPane.showMessageDialog(null, "Product Name only letters!");
        return false;
    }
    if (!containsOnlyLettersAndSpace(merk.getText())) {
        JOptionPane.showMessageDialog(null, "Merk only letters!");
        return false;
    }
    if (Integer.parseInt(harga.getText()) < 10000) {
        JOptionPane.showMessageDialog(null, "Minimum price 10000");
        return false;
    }
    if (Integer.parseInt(idS.getText()) < 1) {
        JOptionPane.showMessageDialog(null, "id > 0");
        return false;
    }
    if (Integer.parseInt(stok.getText()) < 1) {
        JOptionPane.showMessageDialog(null, "Minimum stock 1");
        return false;
    }
    return true;
}

