    private void Tampil() {
        try {
            st = cn.createStatement();
rs = st.executeQuery("SELECT * FROM skincare ORDER BY id");

            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("ID");
            model.addColumn("Merk");
            model.addColumn("Product Name");
            model.addColumn("Stock");
            model.addColumn("Price");
            model.addColumn("Face Category");
//            int no = 1;
            model.getDataVector().removeAllElements();
            model.fireTableDataChanged();
            model.setRowCount(0);
            while (rs.next()) {
                Object[] data = {rs.getString("id"), rs.getString("merk"), rs.getString("nama"), rs.getString("stok"), rs.getString("price"), rs.getString("kategori")};
                model.addRow(data);
                tblData.setModel(model);
//                no++;
            }
//        ------------------
        } catch (Exception e) {
        }
    }

